let yourNumOfDay = parseInt(prompt('Enter num of day'))

if (yourNumOfDay === 1) {
  document.write('Monday')
} else if (yourNumOfDay === 2) {
  document.write('Tuesday')
} else if (yourNumOfDay === 3) {
  document.write('Wednesday')
} else if (yourNumOfDay === 4) {
  document.write('Thursday')
} else if (yourNumOfDay === 5) {
  document.write('Friday')
} else if (yourNumOfDay === 6) {
  document.write('Saturday')
} else {
  document.write('Sunday')
}
