let emotion = parseInt(prompt('Enter your emotion: 1-cheerful, 2-sad, 3-shocked'));

if (emotion === 1) {
  document.write("<img src='./image/cheerful.png' />")
  } else if (emotion === 2) {
  document.write("<img src='./image/sad.jpg' />")
  } else {
  document.write("<img src='./image/shocked.webp' />")
  }
